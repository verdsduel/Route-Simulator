    public TextView textView1,textView2;

    //--------------------------------------------------------
    @Override//MAIN
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_main);

        textView1 = (TextView)findViewById(R.id.txt1);
        textView2 = (TextView)findViewById(R.id.txt2);

        //file manipulation (read)
        File internal = Environment.getExternalStorageDirectory();
        File file = new File(internal,"structure.txt");
        StringBuilder text = new StringBuilder();
        try{
            BufferedReader br = new BufferedReader(new FileReader(file));
            String line;

            while((line = br.readLine()) != null){
                text.append(line);
                text.append('\n');
            }
            br.close();
            textView1.setText('\n'+text.toString());
        }catch(IOException e){
            Toast.makeText(MainActivity.this,e.toString(), Toast.LENGTH_LONG).show();
            textView1.setText(e.toString());
        }
        display();
    }//END MAIN
    //---------------------------------------------------------
    //METHOD
    private void display() {
        String a="";
        int i,j,k;

        for(i=env.length-1; i!=-1; i--){
            for(j=0;j!=env[1].length;j++){
                for(k=0;k!=env[0][1].length;k++)
                    if(env[i][j][k]==pth) a+=" . . ";
                    else if (env[i][j][k]==evxn) a+=" , , ";
                    else if (env[i][j][k]==strs) a+=" HHH ";
                    else if (env[i][j][k]==wall) a+="     ";
                    else if (env[i][j][k]==bldg) a+="     ";
                    else if (env[i][j][k]==9) a+=" @@@ ";
                    else if (env[i][j][k]==4) a+=" !!! ";
                    else a+=env[i][j][k]-orgst;
                a+="\n\n";
            }
            a+="-------------------------------------------------------------------------------------------------------\n\n";
        }
        textView2.setText(a);
    }
